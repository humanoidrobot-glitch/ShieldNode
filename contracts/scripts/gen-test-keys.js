#!/usr/bin/env node
/**
 * Generate Solidity test fixtures for secp256k1 public keys.
 *
 * For each test private key used in Foundry tests, computes the
 * uncompressed secp256k1 public key (64 bytes: x || y) and the
 * corresponding Ethereum address. Outputs a Solidity library.
 *
 * Usage: node scripts/gen-test-keys.js > test/helpers/TestKeys.sol
 *
 * Requires: npm install @noble/secp256k1 (or ethers)
 */

// Use Node.js built-in crypto for secp256k1
const crypto = require('crypto');

// Test private keys used across Foundry test files
const TEST_KEYS = {
  'ENTRY_KEY':    '0xA001',
  'RELAY_KEY':    '0xA002',
  'EXIT_KEY':     '0xA003',
  'CLIENT_KEY':   '0xB001',
  'NODE_KEY':     '0xA101',    // ChallengeManager.t.sol
  'CHALL_KEY':    '0xC0DE',
  'OPERATOR_KEY': '0xDEAD1',   // NodeRegistry.t.sol
};

function privateKeyToPublicKey(pkHex) {
  const pk = pkHex.replace('0x', '').padStart(64, '0');
  const ecdh = crypto.createECDH('secp256k1');
  ecdh.setPrivateKey(Buffer.from(pk, 'hex'));

  // Get uncompressed (65 bytes: 04 || x[32] || y[32]), ensure hex zero-padding.
  const buf = ecdh.getPublicKey(null, 'uncompressed');
  const x = buf.subarray(1, 33).toString('hex').padStart(64, '0');
  const y = buf.subarray(33, 65).toString('hex').padStart(64, '0');
  const full = x + y;

  return { x, y, full };
}

function toSolidityBytes(hex) {
  return `hex"${hex}"`;
}

// Generate output
console.log('// SPDX-License-Identifier: MIT');
console.log('pragma solidity ^0.8.24;');
console.log('');
console.log('/// @title TestKeys');
console.log('/// @notice Pre-computed secp256k1 public keys for test private keys.');
console.log('///         Generated by: node scripts/gen-test-keys.js');
console.log('///         Each key is 64 bytes (x[32] || y[32]), no 0x04 prefix.');
console.log('library TestKeys {');

for (const [name, pk] of Object.entries(TEST_KEYS)) {
  const { x, y, full } = privateKeyToPublicKey(pk);
  console.log(`    /// secp256k1 pubkey for private key ${pk}`);
  console.log(`    function ${name.toLowerCase()}() internal pure returns (bytes memory) {`);
  console.log(`        return ${toSolidityBytes(full)};`);
  console.log(`    }`);
  console.log('');
}

console.log('}');
